export function t(lang: 'en' | 'es' | 'ca', key: string): string {
  const translation = translations[lang] || translations.en;
  const keys = key.split('.');
  let value: any = translation;

  for (const k of keys) {
    value = value?.[k];
  }

  return typeof value === 'string' ? value : key;
}

